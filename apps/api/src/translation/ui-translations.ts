/**
 * Look up a static UI translation. Returns the translated string,
 * or the original Spanish text if no translation exists for that language.
 */
export function getStaticTranslation(text: string, targetLang: string): string | null {
  if (targetLang === 'es') return text;
  const entry = UI_TRANSLATIONS[text];
  if (!entry) return null;
  return entry[targetLang] ?? text;
}

/**
 * Render an email template by key, substituting {name}, {property}, {date} placeholders.
 */
export function renderEmailTemplate(
  key: keyof typeof EMAIL_TEMPLATES,
  lang: string,
  vars: Record<string, string>,
): string {
  const templates = EMAIL_TEMPLATES[key];
  const template = templates[lang] ?? templates['es'];
  return template.replace(/\{(\w+)\}/g, (_, k) => vars[k] ?? `{${k}}`);
}
